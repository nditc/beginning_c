#include <iostream>

using namespace std;

int main() {
    cout << "Add 1 + 1 and get "
         << 1 + 1
         << endl;

    cout << "What about 2 - 1? -> "
         << 2 - 1
         << endl;

    cout << "13 * 12 is so hard! or is it? -> "
         << 13 * 12
         << endl;

    cout << "45 / 4 gives "
         << 45 / 4
         << endl;

    cout << "45 %% 4 gives "
         << 45 % 4
         << "the remainder!"
         << endl;

    return 0;
}
